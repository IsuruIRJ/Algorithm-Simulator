    public boolean getValues() {
        arr = new int[10];
        
        

        if (txtNo1.getText().isEmpty() || txtNo2.getText().isEmpty() || txtNo3.getText().isEmpty() || txtNo4.getText().isEmpty() || txtNo5.getText().isEmpty() || txtNo6.getText().isEmpty() || txtNo7.getText().isEmpty() || txtNo8.getText().isEmpty() || txtNo9.getText().isEmpty() || txtNo10.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all the required fields...!");
            return false;
        } else {
            try {
                arr[0] = Integer.parseInt(txtNo1.getText());
                arr[1] = Integer.parseInt(txtNo2.getText());
                arr[2] = Integer.parseInt(txtNo3.getText());
                arr[3] = Integer.parseInt(txtNo4.getText());
                arr[4] = Integer.parseInt(txtNo5.getText());
                arr[5] = Integer.parseInt(txtNo6.getText());
                arr[6] = Integer.parseInt(txtNo7.getText());
                arr[7] = Integer.parseInt(txtNo8.getText());
                arr[8] = Integer.parseInt(txtNo9.getText());
                arr[9] = Integer.parseInt(txtNo10.getText());

                return true;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Please enter valid numbers...!");
                return false;
            }
        }
    }
    
