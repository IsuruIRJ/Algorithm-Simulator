    public void swap(int num1, int num2) throws InterruptedException{
        for(int i=0; i<15; i++){
            lblArr[num1].setLocation(lblArr[num1].getLocation().x, lblArr[num1].getLocation().y+3);
            lblArr[num2].setLocation(lblArr[num2].getLocation().x, lblArr[num2].getLocation().y-3);
            th.sleep(15);
        }
        int length = lblArr[num2].getLocation().x - lblArr[num1].getLocation().x;
        for(int i=0; i<length; i++){
            lblArr[num1].setLocation(lblArr[num1].getLocation().x+1, lblArr[num1].getLocation().y);
            lblArr[num2].setLocation(lblArr[num2].getLocation().x-1, lblArr[num2].getLocation().y);
            th.sleep(15);
        }
        for(int i=0; i<15; i++){
            lblArr[num1].setLocation(lblArr[num1].getLocation().x, lblArr[num1].getLocation().y-3);
            lblArr[num2].setLocation(lblArr[num2].getLocation().x, lblArr[num2].getLocation().y+3);
            th.sleep(15);
        }
        JLabel temp = lblArr[num1];
        lblArr[num1] = lblArr[num2];
        lblArr[num2] = temp;
    }
    
